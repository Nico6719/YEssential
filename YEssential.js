function randomVividColor() {
    // жҺ’йҷӨз»ҝиүІ(90В°~150В°)е’Ңж·ұзҙ«иүІ(260В°~300В°)
    // еҸҜз”ЁиүІзӣёж®өпјҡ[0,90) [150,260) [300,360) е…ұ 260В°
    const rand = Math.random() * 260;
    let h;
    if      (rand < 90)  h = rand;           // зәў/ж©ҷ/й»„
    else if (rand < 200) h = rand + 60;      // йқ’/и“қ  (150~260)
    else                 h = rand + 100;     // зІү/жҙӢзәў (300~360)

    const s = 0.90 + Math.random() * 0.10;  // 90%~100% й«ҳйҘұе’Ң
    const l = 0.65 + Math.random() * 0.15;  // 65%~80%  й«ҳдә®еәҰ
    const a = s * Math.min(l, 1 - l);
    function f(n) {
        const k = (n + h / 30) % 12;
        return Math.round((l - a * Math.max(-1, Math.min(k - 3, 9 - k, 1))) * 255);
    }
    return [f(0), f(8), f(4)];
}

function generateColorPair() {
    const c1 = randomVividColor();
    let c2, attempts = 0;
    do {
        c2 = randomVividColor();
        const diff = Math.abs(c1[0]-c2[0]) + Math.abs(c1[1]-c2[1]) + Math.abs(c1[2]-c2[2]);
        if (diff > 150 || attempts++ > 20) break;
    } while (true);
    return [c1, c2];
}

