function ranking(plname) {
    let pl = mc.getPlayer(plname);
    if (!pl) return;

    // 1. иҜ»еҸ–зЎ¬зӣҳдёҠзҡ„еҹәзЎҖж•°жҚ®
    let rawFile = moneyranking.read();
    let datas = rawFile ? JSON.parse(rawFile) : {};

    // 2. [е…ій”®дҝ®еӨҚ] еҗҲе№¶еҶ…еӯҳзј“еӯҳдёӯзҡ„е…¶д»–зҺ©е®¶ж•°жҚ®
    for (let name in moneyCache) {
        datas[name] = moneyCache[name];
    }

    // 3. [ж ёеҝғдҝ®еӨҚ] ејәеҲ¶иҺ·еҸ–вҖңдҪ иҮӘе·ұвҖқзҡ„еҪ“еүҚе®һж—¶дҪҷйўқ
    // дёҚз®Ўж–Үд»¶жҲ–зј“еӯҳйҮҢжҳҜеӨҡе°‘пјҢзҺ°еңЁз«ӢеҲ»жҹҘдёҖж¬Ўзңҹе®һзҡ„й’ұ
    let myRealMoney;
    if (economyCfg.isLLMoney) {
        myRealMoney = pl.getMoney(); // LLMoneyжЁЎејҸ
    } else {
        myRealMoney = pl.getScore(economyCfg.scoreboard); // и®ЎеҲҶжқҝжЁЎејҸ
    }

    // еҰӮжһңиҺ·еҸ–еҲ°дәҶдҪҷйўқпјҢејәеҲ¶иҰҶзӣ–иҝӣеҲ—иЎЁпјҢдҝқиҜҒдҪ иҮӘе·ұзңӢеҲ°зҡ„ж•°жҚ®жҳҜ100%жӯЈзЎ®зҡ„
    if (myRealMoney !== undefined && myRealMoney !== null) {
        datas[pl.realName] = myRealMoney;
        
        // йЎәдҫҝжӣҙж–°дёҖдёӢзј“еӯҳпјҢйҳІжӯўдёӢж¬ЎеҸҲеҸҳеӣһеҺ»
        moneyCache[pl.realName] = myRealMoney;
        moneyDirty = true;
    }

    // 4. ж•°жҚ®иҪ¬дёәж•°з»„е№¶жҺ’еәҸ
    let lst = Object.keys(datas).map(name => ({
        name: name,
        money: datas[name]
    }));

    if (lst.length === 0) {
        pl.tell(info + lang.get("no.ranking.data"));
        return;
    }

    // д»ҺеӨ§еҲ°е°ҸжҺ’еәҸ
    lst.sort((a, b) => b.money - a.money);
    
    // жҲӘеҸ–еүҚ50еҗҚ
    const rankingData = lst.slice(0, 50);
    // === жЁЎејҸ 1: иҜҰз»Ҷ UI ===
    if (conf.get("Economy").RankingModel == "New" ) {
        const total = rankingData.reduce((sum, curr) => sum + curr.money, 0);

        let form = mc.newSimpleForm()
            .setTitle(`В§lВ§6в–  иҙўеҜҢжҺ’иЎҢжҰң в–  В§rВ§8[еүҚ${rankingData.length}еҗҚ]`)
            .setContent(
                `В§7жңҚеҠЎеҷЁжҖ»иҙўеҜҢ: В§6${formatMoney(total)}\n` +
                `В§7з»ҹи®Ўж—¶й—ҙ: В§f${new Date().toLocaleTimeString()}\n` +
                `В§6зӮ№еҮ»жҢүй’®иҝ”еӣһиҸңеҚ• | В§aдҪ зҡ„дҪҷйўқ: ${formatMoney(myRealMoney)}\n` +
                `В§8в•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җв•җ`
            );

        rankingData.forEach((v, index) => {
            const rank = index + 1;
            const percentage = total > 0 ? (v.money / total * 100).toFixed(1) : "0.0";
            
            // еҰӮжһңиҝҷдёҖиЎҢжҳҜдҪ иҮӘе·ұпјҢеҠ зІ—жҳҫзӨә
            let entryName = v.name === pl.realName ? `В§eВ§l[жҲ‘] ${v.name}В§r` : v.name;

            form.addButton(
                `${getRankPrefix(rank)} В§l${rank}. В§r${entryName}\n` +
                `В§lВ§cв”ң жҢҒжңү: ${formatMoney(v.money)}` +
                ` В§rВ§lеҚ жҜ”: В§a${percentage}%`
            );
        });

        pl.sendForm(form, (pl, id) => {
            if (id !== null) pl.tell(info + lang.get("money.callback.menu"));
            pl.runcmd("moneygui");
        });

        // ж јејҸеҢ–ж•°еӯ—еҮҪж•°
        function formatMoney(amount) {
            if (amount === undefined || amount === null) return "0";
            if (amount >= 1e6) return (amount / 1e6).toFixed(1) + "M";
            if (amount >= 1e3) return (amount / 1e3).toFixed(1) + "K";
            return amount.toLocaleString();
        }

        // жҺ’еҗҚеӣҫж ҮеҮҪж•°
        function getRankPrefix(rank) {
            return ["В§bвҳҶ", "В§cв—Ҷ", "В§aв–Ј"][Math.min(2, rank - 1)] || "В§7";
        }
    } 
    // === жЁЎејҸ 0: з®ҖеҚ•ж–Үжң¬еҲ—иЎЁ ===
    else {
        let form = mc.newSimpleForm();
        let str = '';
        rankingData.forEach((v) => {
            str += `${v.name}: ${v.money}\n`;
        });
        form.setTitle(lang.get("ranking.list"));
        form.setContent(str);
        pl.sendForm(form, (pl, id) => {
            if (id == null) pl.runcmd("moneygui");
        });
    }
}
